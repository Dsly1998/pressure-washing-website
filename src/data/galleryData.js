export const getGallerySlides = () => {
  return galleryData.map((item) => {
    if (item.type === "single") {
      return [
        {
          image: item.image,
          alt: item.alt,
          caption: item.caption,
        },
      ];
    }
    return [
      {
        image: item.before.image,
        alt: item.before.alt,
        caption: item.before.caption,
      },
      {
        image: item.after.image,
        alt: item.after.alt,
        caption: item.after.caption,
      },
    ];
  });
};
